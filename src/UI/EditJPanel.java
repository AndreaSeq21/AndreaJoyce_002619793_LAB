    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        String id = packageId1.getText();
        String weight = packageWeight1.getText();
        String custName = customerName1.getText();
        String custId = customerId1.getText();
        this.delPackage.setPackageId(Integer.valueOf(id));
        this.delPackage.setPackageWeight(Integer.valueOf(weight));
        Customer customer = this.delPackage.getCustomer();
        customer.setCustomerId(Integer.valueOf(custId));
        customer.setFullName(custName);
        
        
    }//GEN-LAST:event_updateButtonActionPerformed

